package bg.tu_varna.sit.b1.f23621713;

/**
 * Интерфейс за командите в приложението.
 * Всички команди (като open, save, create и др.) трябва да го имплементират.
 * Дефинира един метод – execute – който приема аргументи от потребителския вход.
 */

public interface Command {
    /**
     * Изпълнява конкретна команда, базирана на подадените аргументи.
     *
     * @param parts масив от въведени параметри (напр. команда и стойности)
     */

    void execute(String[] parts);
}
